//! Built-in user app loader.
//!
//! 现在先用内置机器码数组，后面可以替换成链接脚本里的 app section。

#[cfg(target_arch = "loongarch64")]
pub static USER_APP_0: &[u8] = &[
    // li.w/d a0, 0
    0x04, 0x00, 0xc0, 0x02,
    // li.w/d a1, 0
    0x05, 0x00, 0xc0, 0x02,
    // li.w/d a2, 0
    0x06, 0x00, 0xc0, 0x02,
    // li.w/d a7, 0
    0x0b, 0x00, 0xc0, 0x02,
    // syscall 0
    0x00, 0x00, 0x2b, 0x00,
    // b .
    0x00, 0x00, 0x00, 0x50,
];

#[cfg(target_arch = "loongarch64")]
pub static USER_APP_1: &[u8] = &[
    // li.w/d a0, 1
    0x24, 0x00, 0xc0, 0x02,
    // li.w/d a1, 0
    0x05, 0x00, 0xc0, 0x02,
    // li.w/d a2, 0
    0x06, 0x00, 0xc0, 0x02,
    // li.w/d a7, 0
    0x0b, 0x00, 0xc0, 0x02,
    // syscall 0
    0x00, 0x00, 0x2b, 0x00,
    // b .
    0x00, 0x00, 0x00, 0x50,
];

#[cfg(target_arch = "riscv64")]
pub static USER_APP_0: &[u8] = &[
    // li a0, 0
    0x13, 0x05, 0x00, 0x00,
    // li a1, 0
    0x93, 0x05, 0x00, 0x00,
    // li a2, 0
    0x13, 0x06, 0x00, 0x00,
    // li a7, 0
    0x93, 0x08, 0x00, 0x00,
    // ecall
    0x73, 0x00, 0x00, 0x00,
    // j .
    0x6f, 0x00, 0x00, 0x00,
];

#[cfg(target_arch = "riscv64")]
pub static USER_APP_1: &[u8] = &[
    // li a0, 1
    0x13, 0x05, 0x10, 0x00,
    // li a1, 0
    0x93, 0x05, 0x00, 0x00,
    // li a2, 0
    0x13, 0x06, 0x00, 0x00,
    // li a7, 0
    0x93, 0x08, 0x00, 0x00,
    // ecall
    0x73, 0x00, 0x00, 0x00,
    // j .
    0x6f, 0x00, 0x00, 0x00,
];

pub fn num_apps() -> usize {
    2
}

pub fn get_app_data(app_id: usize) -> &'static [u8] {
    match app_id {
        0 => USER_APP_0,
        1 => USER_APP_1,
        _ => panic!("bad app id {}", app_id),
    }
}